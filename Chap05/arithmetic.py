x = 5
y = 3
# Addition
z = x + y

print(f'result is {z}')

# Subtraction
z = x - y

print(f'result is {z}')

# Multiplication
z = x * y

print(f'result is {z}')

# Division
z = x / y

print(f'result is {z}')

# Integer Division
z = x // y

print(f'result is {z}')

# Modular
z = x % y

print(f'result is {z}')